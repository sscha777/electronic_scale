void introLoop(void)
{
    static uint32_t tick = 0;
    uint8_t TouchTag = TouchInput();

    if (TouchTag == INTRO_ENTER)
    {
        //ESP_LOGI(TAG, "Touch(%d)\n", TouchTag);
        (*Touch_Callback)(TouchTag);
    }

    tick++;
    if (tick > 50)
    {
        //ESP_LOGE(TAG, "tick 100");
        newScenario = MAIN_SCALE_SCENARIO;
        tick = 0;
        EVE_bitmap_load();
    }
}

void IntroTouchCallback(INTRO_EVENTS event)
{
    //ESP_LOGE(TAG, "initTouchCallback");

    switch (event)
    {
    case INTRO_ENTER:
        newScenario = MAIN_SCALE_SCENARIO;
        break;
    default:
        break;
    }

    if (event == INTRO_ENTER)
    {
        EVE_sound_play(0x50, AUDIO_VOLUME);
    }
}

